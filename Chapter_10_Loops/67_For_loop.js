// what is for loop

// for loop is a control flow statement that allows you to execute a block of code repeatedly 
// until a certain condition is met. It consists of three parts: initialization, condition, and increment/decrement. The syntax of a for loop is as follows: 
// for (initialization; condition; increment/decrement) {
//     // code to be executed
// }

// example of for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}