//Function- Reusablecode that can be called multiple times.
//1.define the function and 2.call the function

//define
function greet(name) {
    console.log(`Hello, ${name}!`); //console.log('Hello, ' + name + '!'); is an alternative way to concatenate strings
}

//call
greet("Pravallika K"); //returns "Hello, Pravallika K!" 
greet("Automation"); //returns "Hello, Automation!"
