//what is statement in JavaScript?// A statement is a piece of code that performs a specific action. 
// It can be a declaration, an expression, or a control flow statement. 
// Statements are executed sequentially in the order they appear in the code.
let x = 10; // This is a variable declaration statement
console.log(x); // This is an expression statement that outputs the value of x

//what is if-else statement in JavaScript?
// if-else statement is used to execute a block of code if a specified condition is true, 
// and another block of code if the condition is false.
let age = 20;
if (age >= 18) {
    console.log("You are an adult.");
} else {
    console.log("You are a minor.");
}