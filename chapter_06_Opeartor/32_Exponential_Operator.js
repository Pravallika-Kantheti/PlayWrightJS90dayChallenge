//exponential operator
//The exponential operator (**) is used to perform exponentiation, which is the process of raising a base to a power.
let base = 2;
let exponent = 3;
let result = base ** exponent;
console.log(result);
// Output: 8 (2 raised to the power of 3 is 8)


//You can also use the exponential operator with variables and expressions:
let x = 5;
let y = 2;
console.log(x ** y);
// Output: 25 (5 raised to the power of 2 is 25)