//logical operators
//Logical operators are used to combine multiple boolean expressions and return a boolean result based on the combination.
//The basic logical operators are: && (logical AND), || (logical OR), and ! (logical NOT).  
let p = true;
let q = false;
console.log("logical AND (&&): " + (p && q)); // Output: false (true AND false is false)
console.log("logical OR (||): " + (p || q)); // Output: true (true OR false is true)
console.log("logical NOT (!): " + (!p)); // Output: false (NOT true is false)   
console.log("logical NOT (!): " + (!q)); // Output: true (NOT false is true)
